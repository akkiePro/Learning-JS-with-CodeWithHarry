// Types of Primitive Data Types:
// NN BB SS U
// N - Null
// N - Number
// B - Boolean
// B - BigInt
// S - String
// S - Symbol
// U - Undefined

// console.log("primitive data types")
let a = null
console.log(a)
console.log(typeof a)

let b = 12
console.log(b)
console.log(typeof b)

let c = true
console.log(c)
console.log(typeof c)

let d = BigInt(12)
console.log(d)
console.log(typeof d)

let e = "Hello"
console.log(e)
console.log(typeof e)

let f = Symbol("I am a nice symbol")
console.log(f)
console.log(typeof f)

let g = undefined
// let g  - it's also undefined
console.log(g)
console.log(typeof g)